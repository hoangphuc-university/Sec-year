-- Tạo bảng LOI
CREATE TABLE LOI (
    MALOI VARCHAR2(10) PRIMARY KEY,
    NOIDUNG VARCHAR2(255),
    MUCTIENPHAT NUMBER(10, 2)
);

-- Tạo bảng DOITUONG
CREATE TABLE DOITUONG (
    MADT VARCHAR2(10) PRIMARY KEY,
    HOTEN VARCHAR2(100),
    CMND VARCHAR2(20),
    DIACHI VARCHAR2(255),
    BIENKS VARCHAR2(20),
    TONGTIENPHAT NUMBER(15, 2)
);

-- Tạo bảng VIPHAM
CREATE TABLE VIPHAM (
    MALOI VARCHAR2(10),
    MADT VARCHAR2(10),
    THOIDIEMVP DATE,
    NGAYHEN DATE,
    PRIMARY KEY (MALOI, MADT, THOIDIEMVP),
    FOREIGN KEY (MALOI) REFERENCES LOI(MALOI),
    FOREIGN KEY (MADT) REFERENCES DOITUONG(MADT)
);
